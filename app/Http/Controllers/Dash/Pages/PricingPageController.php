     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {

        $pricingPages = PricingPage::findOrFail($id);

        $pricingPage = $request->all();

        if ($request->hasFile('image')) {

            $pricingPage['image'] = $request->file('image')->store('pricing', 'public');

        } else {
            unset($pricingPage['image']);
        }

        $pricingPages->update($pricingPage);

        return back()->with('success', 'Pricing Page Information Updated successfully');


    }

